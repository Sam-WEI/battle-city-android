package com.samwdev.battlecity.utils

import android.util.Log

private const val TAG = "BattleCity"

private fun logD(msg: String) {
    Log.d(TAG, msg)
}

private fun logI(msg: String) {
    Log.i(TAG, msg)
}

private fun logW(msg: String) {
    Log.w(TAG, msg)
}

private fun logE(msg: String) {
    Log.e(TAG, msg)
}